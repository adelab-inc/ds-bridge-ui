/**
 * 동적 name/size로 Icon을 생성하는 헬퍼.
 * Storybook 등에서 런타임에 name/size가 결정될 때 as any 없이 사용.
 */
export const createIcon = (name: string, size: IconSize) =>
  React.createElement(Icon, { name, size } as IconProps);

export const Icon = ({ name, size = 24, className, ...props }: IconProps) => {
  // Quick 아이콘 처리 (48x48, 다중 색상)
  if (name.startsWith('quick-')) {
    const icon = quickIcons[name as QuickIconName];
    if (!icon) {
      // eslint-disable-next-line no-console
      console.error(`Icon "${name}" not found.`);
      return null;
    }
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        viewBox={icon.viewBox}
        fill="none"
        className={className}
        {...props}
      >
        {icon.path}
      </svg>
    );
  }

  // 사이즈별 아이콘 조회
  const sizeKey = (size || 24) as IconSize;
  const icons = iconMap[sizeKey];
  const icon = icons?.[name];

  if (!icon) {
    // eslint-disable-next-line no-console
    console.error(`Icon "${name}" (size=${sizeKey}) not found.`);
    return null;
  }

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={sizeKey}
      height={sizeKey}
      viewBox={`0 0 ${sizeKey} ${sizeKey}`}
      fill="currentColor"
      className={className}
      {...props}
    >
      {icon.path}
    </svg>
  );
};

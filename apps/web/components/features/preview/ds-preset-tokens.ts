export const DS_CANVAS_COLOR = DS_TAILWIND_COLORS['bg-canvas'];

/**
 * AI 생성 코드가 DS 네이밍의 짧은 형태(`bg-canvas`, `bg-surface`, `border-default`,
 * `text-primary` 등)를 쓸 때도 해석되도록 명시적 CSS 유틸리티를 생성.
 *
 * 배경:
 * - DS 프리셋의 컬러 키는 이미 `bg-canvas`, `border-default` 같은 풀 이름을 쓰므로
 *   Tailwind 프리픽스 규칙상 `bg-bg-canvas`, `border-border-default` 처럼 이중으로
 *   생성된다. DS 내부 컴포넌트는 이 이중 네이밍(`bg-bg-surface`, `text-text-primary`)을
 *   따르지만, AI가 LLM으로 생성한 코드는 흔히 짧은 형태를 사용해 매칭이 실패한다.
 * - Tailwind Play CDN 의 JIT 에 의존하지 않고 직접 CSS 규칙을 발행하여 안정적으로 적용.
 *
 * 생성 규칙:
 * - `bg-*`       → `.bg-*        { background-color: value }`
 * - `border-*`   → `.border-*    { border-color: value }`
 * - `text-*`     → `.text-*      { color: value }`
 * - `icon-*`     → `.text-icon-* { color: value }` (아이콘은 보통 color 로 사용)
 *
 * 나머지 카테고리(`brand-`, `tint-`, `hue-`, `chart-` 등)는 Tailwind 프리픽스 시스템을
 * 그대로 활용하도록 두고 별도 유틸을 만들지 않는다.
 */
export const DS_TOKEN_UTILITY_CSS: string = (() => {
  const rules: string[] = [];

  // 동적 상태 (hover/focus/active/disabled) 클래스는 Tailwind Play CDN 의 JIT 가
  // 때로 누락할 수 있으므로, DS 토큰에 한해 명시적으로 raw CSS 로 발행해 100% 보장.
  // Tailwind 의 escape 규칙(콜론을 `\\:` 로 처리)을 그대로 따름.
  const STATE_VARIANTS: Array<{ prefix: string; pseudo: string }> = [
    { prefix: '', pseudo: '' },
    { prefix: 'hover\\:', pseudo: ':hover' },
    { prefix: 'focus\\:', pseudo: ':focus' },
    { prefix: 'focus-visible\\:', pseudo: ':focus-visible' },
    { prefix: 'active\\:', pseudo: ':active' },
    { prefix: 'disabled\\:', pseudo: ':disabled' },
  ];

  const emit = (cls: string, property: string, value: string) => {
    for (const { prefix, pseudo } of STATE_VARIANTS) {
      rules.push(`.${prefix}${cls}${pseudo} { ${property}: ${value}; }`);
    }
  };

  for (const [key, value] of Object.entries(DS_TAILWIND_COLORS)) {
    if (key.startsWith('bg-')) {
      emit(key, 'background-color', value);
    } else if (key.startsWith('border-')) {
      emit(key, 'border-color', value);
    } else if (key.startsWith('text-')) {
      emit(key, 'color', value);
    } else if (key.startsWith('icon-')) {
      emit(`text-${key}`, 'color', value);
    }
  }
  return rules.join('\n');
})();
